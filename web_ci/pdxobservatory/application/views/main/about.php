          <h2>About</h2>
          <p>Technology for sensors and mobile devices is improving and their costs are dropping. New data sources are being digitized. Data storage costs are dropping. Trends such as these are leading to increases in data volume and data variety leading to new demands for analysis of multiple, varied, fused data sources. For example, city planners face demands to perform analysis for data-centric decision-making and performance measures. In addition, the general public has access to data sources unthinkable even just a few years ago.</p>

          <p>The Portland Observatory applies Big Data technology to Smart Cities with the goal of improving the safety, sustainability and effectiveness of Portland and other cities. The Portland Observatory has selected the Smart Cities domain because it expresses the Variety axis of Big Data.</p>

          <p>The Portland Observatory:</p>
            <ul>
              <li>Collects and organizes Smart Cities-related data</li>
              <li>Serves researchers, planners, managers, and the public by providing resources to inform planning and decision making</li>
              <li>Enables data Usability and Re-usability through the Decision Workbench</li>
            </ul>

          <p>The Portland Observatory is supported by funding from a Maseeh Professorship and the Intel Science and Technology Center for Big Data</p>          
